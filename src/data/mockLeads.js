// ---- Derived lead dates (for month-based analytics) ------------------------
// Real backends store these; the mock spreads them deterministically over recent
// months so the month filter has data to show.
//   assignedDate — when the lead was assigned to its DSC (all leads)
//   closedDate   — when a won lead was closed (won leads only; "" otherwise)
// TODO(backend): return real assignedDate/closedDate on each lead.
const _WON = new Set(["Won", "Project Started", "Project Delivered", "Closed"]);
const REF_YEAR = 2026;
const REF_MONTH = 7; // July 2026 — the baseline "current month" for the mock data
function _ym(monthsBack, day) {
  let m = REF_MONTH - monthsBack;
  let y = REF_YEAR;
  while (m <= 0) {
    m += 12;
    y -= 1;
  }
  const d = Math.min(28, Math.max(1, day));
  return `${y}-${String(m).padStart(2, "0")}-${String(d).padStart(2, "0")}`;
}
function _num(id) {
  const n = parseInt(String(id).replace(/\D/g, ""), 10);
  return Number.isNaN(n) ? 0 : n;
}

// ---- Deal enrichment (Company → Deal model, commission line items) ----------
// A won deal carries "line items" (offeringId + amount) so the commission engine
// (lib/commission) can price it against the catalog. Seed ids mirror the
// compensation catalog (lib/compConfig). Everything here is mock seeding; a real
// backend stores line items directly on the deal.
// Maps a service/product NAME (as used in the leads' service arrays) to its
// catalog offering id. Exported so the Deal layer (data/mockDeals, lib/dealsApi)
// can turn a lead's interest into deals. A real backend joins on offering ids.
export const OFFERING_ID_BY_NAME = {
  "Custom Software": "svc-custom-software",
  "Website Development": "svc-website",
  "Digital Marketing": "svc-digital-marketing",
  "AI Tools": "svc-ai-tools",
  "Mobile App": "svc-mobile-app",
  "SaaS Subscription": "prd-saas-subscription",
};

// A stable company id from the company name, so deals for the same company can be
// grouped (a company can have many deals over time).
function _companyId(company) {
  return (
    "co-" +
    String(company || "")
      .toLowerCase()
      .replace(/[^a-z0-9]+/g, "-")
      .replace(/^-|-$/g, "")
  );
}

// Under the Lead → Deal model the LEAD carries NO money. Quoted/Closed/Discount/
// Lost Reason all live on the Deal — so we strip them here (along with the
// legacy Company→Deal fields lineItems/interestedOfferingIds/wonApprovedDate).
// The three service arrays (Pitched/Interested/Onboarded) stay: they are the
// DSC's knowledge tags, and Services Interested drives which offerings a deal can
// be created for. `assignedDate`/`closedDate` stay (dates, used to seed deals).
function withDates(lead) {
  // eslint-disable-next-line no-unused-vars
  const { quotedAmount, closedAmount, lostReason, ...rest } = lead;
  const n = _num(lead.leadId);
  const back = n % 4; // assigned 0–3 months ago
  const assignedDate = _ym(back, ((n * 7) % 27) + 1);
  const won = _WON.has(lead.leadStatus);
  const closedDate = won
    ? _ym(back === 0 ? 0 : n % (back + 1), ((n * 5) % 27) + 1) // same month or newer
    : "";
  return {
    ...rest,
    companyId: _companyId(lead.company),
    assignedDate,
    closedDate,
  };
}

